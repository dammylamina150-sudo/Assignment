#include <stdio.h>

/**
 * main - Demonstrates arithmetic operators in C
 *
 * Return: Always 0
 */
int main(void)
{
    int x = 12, y = 4;

    printf("x = %d, y = %d\n", x, y);

    printf("x + y = %d\n", x + y);
    printf("x - y = %d\n", x - y);
    printf("x * y = %d\n", x * y);
    printf("x / y = %d\n", x / y);
    printf("x %% y = %d\n", x % y);

    return 0;
}
